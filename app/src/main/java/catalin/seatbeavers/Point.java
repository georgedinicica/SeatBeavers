public class Point {
    float xCoord;
    float yCoord;

    public Point(float xCoord, float yCoord) {
        this.xCoord = xCoord;
        this.yCoord = yCoord;
    }

    public float getyCoord() {
        return yCoord;
    }
    public float getxCoord() {
        return xCoord;
    }
}
